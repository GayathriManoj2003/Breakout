package Breakout.model;

import java.awt.Rectangle;

public class Paddle {
    private int posX;
    private int posY;
    private int width;
    private int height;

    public Paddle(int posX, int posY, int width, int height) {
        this.posX = posX;
        this.posY = posY;
        this.width = width;
        this.height = height;
    }

    public int getPosX() {
        return posX;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public int getPosY() {
        return posY;
    }

    public void setPosY(int posY) {
        this.posY = posY;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
    
 // Method to get the bounds of the paddle
    public Rectangle getBounds() {
        return new Rectangle(posX, posY, 10, 10);
    }
}
